from enum import Enum

from binance.enums import KLINE_INTERVAL_1MINUTE, KLINE_INTERVAL_3MINUTE, KLINE_INTERVAL_5MINUTE, \
    KLINE_INTERVAL_15MINUTE, KLINE_INTERVAL_30MINUTE, KLINE_INTERVAL_1HOUR, KLINE_INTERVAL_2HOUR, KLINE_INTERVAL_4HOUR, \
    KLINE_INTERVAL_6HOUR, KLINE_INTERVAL_8HOUR, KLINE_INTERVAL_12HOUR, KLINE_INTERVAL_1DAY, KLINE_INTERVAL_3DAY, \
    KLINE_INTERVAL_1WEEK, KLINE_INTERVAL_1MONTH


class Interval(Enum):
    MINUTE1 = (KLINE_INTERVAL_1MINUTE, 60)
    MINUTE3 = (KLINE_INTERVAL_3MINUTE, 3 * 60)
    MINUTE5 = (KLINE_INTERVAL_5MINUTE, 5 * 60)
    MINUTE15 = (KLINE_INTERVAL_15MINUTE, 15 * 60)
    MINUTE30 = (KLINE_INTERVAL_30MINUTE, 30 * 60)
    HOUR1 = (KLINE_INTERVAL_1HOUR, 60 * 60)
    HOUR2 = (KLINE_INTERVAL_2HOUR, 2 * 60 * 60)
    HOUR4 = (KLINE_INTERVAL_4HOUR, 4 * 60 * 60)
    HOUR6 = (KLINE_INTERVAL_6HOUR, 6 * 60 * 60)
    HOUR8 = (KLINE_INTERVAL_8HOUR, 8 * 60 * 60)
    HOUR12 = (KLINE_INTERVAL_12HOUR, 12 * 60 * 60)
    DAY1 = (KLINE_INTERVAL_1DAY, 24 * 60 * 60)
    DAY3 = (KLINE_INTERVAL_3DAY, 3 * 24 * 60 * 60)
    WEEK1 = (KLINE_INTERVAL_1WEEK, 7 * 24 * 60 * 60)
    MONTH1 = (KLINE_INTERVAL_1MONTH, 30 * 24 * 60 * 60)

    def __init__(self, abbreviate, time):
        self.abbreviate = abbreviate
        self.time = time
